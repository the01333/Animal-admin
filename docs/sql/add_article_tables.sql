  `id` bigint NOT NULL AUTO_INCREMENT,
  `title` varchar(255) NOT NULL COMMENT '标题',
  `excerpt` varchar(500) COMMENT '摘要',
  `content` longtext NOT NULL COMMENT 'Markdown格式的内容',
  `image` varchar(500) COMMENT '封面图片URL',
  `category` varchar(100) NOT NULL COMMENT '分类',
  `views` int DEFAULT 0 COMMENT '浏览次数',
  `created_at` timestamp DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` timestamp DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  KEY `idx_category` (`category`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='领养指南表';

